package Selenium_FrameWork;

public class do_while_loop_p_1 {

	// do while loop is used to execute only one body at a time
	public static void main(String[] args) {

		int d = 1;

		do {
			System.out.println(d);
			d++;
		} while (d < 5);

		System.out.println("do while loop finished here");
	}

}
