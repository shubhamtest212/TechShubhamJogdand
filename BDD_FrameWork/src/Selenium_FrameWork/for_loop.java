package Selenium_FrameWork;

// loop- for loop, for each loop, while loop & do-while loop
// for loop- if the iteration is fixed use for loop
// for each loop- in the case of Array traversing
// while loop- if the iteration is not fixed
// do-while loop- if we want to execute body one time

public class for_loop {

	public static void main(String[] args) {

		for (int i = 0; i <= 5; i++) {
			System.out.println(i);
		}
		System.out.println("Control came out from loop");

	}

}

// for loop- if the iteration is fixed then used for loop
