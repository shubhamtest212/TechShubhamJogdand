package Selenium_FrameWork;

public class Java_Operators_Unary {

	// Unary Operator is dependes on Pre-Increment, Post-Increment, Pre-Decrement,
	// Post-Decrement
	public static void main(String[] args) {

		int a = 5;

		System.out.println(a); // 5
		System.out.println(a++); // 5 Pre-Increment
		System.out.println(a); // 6
		System.out.println(++a); // 7 Post-Increment
		System.out.println(a--); // 7 Pre-Decrement
		System.out.println(a); // 6
		System.out.println(--a); // 5 Post-Decrement

	}

}
