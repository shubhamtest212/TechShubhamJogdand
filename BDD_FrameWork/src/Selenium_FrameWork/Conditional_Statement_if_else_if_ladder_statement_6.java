package Selenium_FrameWork;

public class Conditional_Statement_if_else_if_ladder_statement_6 {

	public static void main(String[] args) {

		// if else if ladder statement

		int x = 600;
		int y = 300;

		if (x < y) {
			System.out.println("x is less than y");
		} else if (x == y) {
			System.out.println("x is equal to y");
		} else {
			System.out.println("x is not equal to y");
		}

	}

}

// This is known as ladder statement if else if statement
// if there is another one condition for else if statement is considering as ladder statement
