package Selenium_FrameWork;

public class FOR_LOOP1 {

	public static void main(String[] args) {

		// For loop- If the iteration is fixed use for loop
		// In the above programme iteration is fixed i.e. i <= 10

		for (int i = 0; i <= 10; i++) {
			System.out.println(i);
		}
		System.out.println("Condition gets false and loop came out from body");

	}

}
