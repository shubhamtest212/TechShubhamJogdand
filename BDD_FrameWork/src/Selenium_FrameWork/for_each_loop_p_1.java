package Selenium_FrameWork;

public class for_each_loop_p_1 {

	public static void main(String[] args) {

		String abc[] = { "SHUBHAM", "SANTOSH", "JOGDAND" };
		System.out.println(abc.length);

		// for each loop used for Array traversing

		for (String xyz : abc) {
			System.out.println(xyz);
		}

	}

}
