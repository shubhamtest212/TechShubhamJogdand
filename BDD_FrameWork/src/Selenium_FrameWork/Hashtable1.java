package Selenium_FrameWork;

import java.util.Hashtable;

public class Hashtable1 {

	public static void main(String[] args) {

		// HashTable

		Hashtable<Integer, String> hmm = new Hashtable<>();
		hmm.put(1, "JANUARY");
		hmm.put(2, "FEBRUARY");
		hmm.put(3, "MARCH");
		System.out.println(hmm.get(1));
		System.out.println(hmm.get(2));
		System.out.println(hmm.get(3));

	}

}


