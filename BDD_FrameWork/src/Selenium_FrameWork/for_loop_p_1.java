package Selenium_FrameWork;

public class for_loop_p_1 {

	public static void main(String[] args) {

		// If the iteration is fixed then used for loop
		for (int i = 0; i < 10; i++) {
			System.out.println(i);
		}

		System.out.println("end of for loop here");

	}

}

// If the iteration is fixed then used for loop
// For Array traversing used for each loop
// If the iteration is not fixed then used while loop
// Want to execute body at one time then used do while loop