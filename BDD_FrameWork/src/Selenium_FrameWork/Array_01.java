package Selenium_FrameWork;

public class Array_01 {

	public static void main(String[] args) {

		// Array
		int a[] = new int[10]; // Defining an int array
		int b[] = new int[10]; // Defining an int array
		int c[] = new int[10]; // Defining an int array
		int d[] = new int[10]; // Defining an int array

		System.out.println(a.length); // Length of int a array
		System.out.println(b.length); // Length of int b array
		System.out.println(c.length); // Length of int c array
		System.out.println(d.length); // Length of int d array

		a[0] = 100;
		b[0] = 200;
		c[0] = 300;
		d[0] = 400;

		System.out.println(a[0]);
		System.out.println(b[0]);
		System.out.println(c[0]);
		System.out.println(d[0]);

	}

}
