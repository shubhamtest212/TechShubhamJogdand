package Selenium_FrameWork;

public class LOOP1 {

	public static void main(String[] args) {

		// Loop Concept
		// for loop = if the iteration is fixed
		// for each loop= can use for array traversing
		// while loop= if the iteration is not fixed
		// do-while loop= if we want to execute body one time

		// FOR LOOP Concept

		for (int i = 1; i <= 5; i++) {
			System.out.println(i);

		}
		System.out.println("Control came out from loop");

	}

}
