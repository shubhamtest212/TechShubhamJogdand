package Selenium_FrameWork;

public class FOR_EACH_LOOP3 {

	public static void main(String[] args) {

		// for each loop use for array traversing
		// int Array
		int xyz[] = { 12, 6, 1999 };

		// print int array length
		System.out.println(xyz.length);

		for (int abc : xyz) {
			System.out.println(abc);
		}

	}

}
